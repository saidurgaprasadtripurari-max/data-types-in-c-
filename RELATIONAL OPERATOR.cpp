// RELATIONAL OPERATOR
#include <stdio.h>
#include <conio.h>

int main() {
    int a = 20, b = 10;

    printf("%d\n", a <  b); // LESS THAN
    printf("%d\n", a >  b); // GREATER THAN
    printf("%d\n", a <= b); // LESS THAN OR EQUAL
    printf("%d\n", a >= b); // GREATER THAN OR EQUAL
    printf("%d\n", a != b); // NOT TRUE
    printf("%d\n", a == b); // COMPARISION 
    

    return 0;
}

